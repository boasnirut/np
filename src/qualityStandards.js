export const qualityLevelMap = Object.fromEntries(qualityLevels.map((level) => [level.id, level]))

export function qualityIndicator(levelId, code) {
  const level = qualityLevelMap[levelId]
  return level?.standards.flatMap((standard) => standard.indicators).find((indicator) => indicator.code === code)
}
